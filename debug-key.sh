#!/bin/bash

# Скрипт для проверки key.json

echo "=========================================="
echo "Проверка key.json"
echo "=========================================="

if [ -f "key.json" ]; then
    echo "Файл key.json существует"
    cat key.json | jq -r '.service_account_id'
else
    echo "⚠️  Файл key.json не найден"
fi

echo ""
echo "=========================================="
echo "Проверка завершена"
echo "=========================================="
